XfromAlphaBeta[alphabeta_]:=Module[{alpha1,et1,sq1,phi,tau1,tau2},
  If[!ArrayQ[alphabeta] || Length[alphabeta]!=2, Return[{}]];
  alpha1=1-alphabeta; sq1=Sqrt[alpha1[[1]]];
  phi=ArcCos[ (3*alpha1[[1]]-alpha1[[2]])/(2*alpha1[[1]]*sq1) ]/3; 
  tau1=(1-sq1*Cos[phi])/3; tau2=sq1*Sin[phi]/Sqrt[3];
  {tau1+tau2,tau1-tau2}
]; 
